bool URockGameplayAbility::CanChangeActivationGroup(ERockAbilityActivationGroup NewGroup) const
{
	if (!IsInstantiated() || !IsActive())
	{
		return false;
	}

	if (GetActivationGroup() == NewGroup)
	{
		return true;
	}

	const URockAbilitySystemComponent* ASC = GetRockAbilitySystemComponentFromActorInfo();
	check(ASC);

	if ((GetActivationGroup() != ERockAbilityActivationGroup::Exclusive_Blocking) && ASC->IsActivationGroupBlocked(NewGroup))
	{
		// This ability can't change groups if it's blocked (unless it is the one doing the blocking).
		return false;
	}

	if ((NewGroup == ERockAbilityActivationGroup::Exclusive_Replaceable) && !CanBeCanceled())
	{
		// This ability can't become replaceable if it can't be canceled.
		return false;
	}

	return true;
}

bool URockGameplayAbility::ChangeActivationGroup(ERockAbilityActivationGroup NewGroup)
{
	ENSURE_ABILITY_IS_INSTANTIATED_OR_RETURN(ChangeActivationGroup, false);

	if (!CanChangeActivationGroup(NewGroup))
	{
		return false;
	}

	if (GetActivationGroup() != NewGroup)
	{
		URockAbilitySystemComponent* ASC = GetRockAbilitySystemComponentFromActorInfo();
		check(ASC);
	
		ASC->RemoveAbilityFromActivationGroup(GetActivationGroup(), this);
		ASC->AddAbilityToActivationGroup(NewGroup, this);
	
		ActivationGroup = NewGroup;
	}

	return true;
}


